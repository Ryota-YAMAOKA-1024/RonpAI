def get_phase_prompt(anger_level: int) -> str:
    """指定された怒りレベルのフェーズ定義を、プロンプト用テキストに変換する"""
    phase = ANGER_PHASES.get(anger_level, ANGER_PHASES[0])
    lines = [
        ANNOYING_TECHNIQUES,
        f"## 現在の怒りフェーズ: レベル {anger_level}「{phase['label']}」",
        f"状況: {phase['description']}",
        f"AIの口調: {phase['ai_tone']}",
        "",
        "### このフェーズで優先するテクニック",
    ]
    if phase["technique_priority"]:
        for t in phase["technique_priority"]:
            lines.append(f"- {t}")
    else:
        lines.append("- なし（素直に降参する）")
    lines += [
        "",
        "### 発話例（口調の参考のみ。この文言を直接コピーしてはならない。会話の流れに沿った独自の返答を生成すること）",
    ]
    for phrase in phase["sample_phrases"]:
        lines.append(f"- {phrase}")
    lines.append("")
    lines.append(f"### 制約: {phase['constraints']}")
    return "\n".join(lines)


def assess_anger_increase(current_level: int, max_level: int = 3) -> bool:
    """怒りレベルが上昇可能かを確認"""
    return current_level < max_level
